.data
A: .word 0,10,-3,40,-15,0,2,-4,9
.text
LDR R0,=A
MOV R1,#9
MOV R5,#0
MOV R4,#0
MOV R3,#0
LOOP:LDR R2,[R0],#4
	CMP R2,#0
	BEQ ZERO
	BMI NEG
	ADD R5,R5,#1
	SUBS R1,R1,#1
	BEQ EXIT
	BNE LOOP
ZERO:ADD R4,R4,#1
	SUBS R1,R1,#1
	BEQ EXIT
	BNE LOOP
NEG:
	ADD R3,R3,#1
	SUBS R1,R1,#1
	BEQ EXIT
	BNE LOOP
EXIT:SWI 0X01
.end 