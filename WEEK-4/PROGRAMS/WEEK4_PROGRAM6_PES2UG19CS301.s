.data
A: .WORD 1,2,3,4,5,6,7,8,9

.text 
LDR R0, =A

MOV R1, #0; SUM  
MOV R3, #0; ROW #
MOV R4, #3; TOTAL COLUMNS #
MOV R5, #0; INDEX COUNT IN EACH ROW
MOV R9, #9; TOTAL NUMBER OF ELEMENTS
LOOP:
	MLA R10, R3, R4, R5
	LDR R6, [R0, R10, LSL #2]
	ADD R1, R1, R6
	ADD R5, R5, #1 
	SUBS R9, R9, #1
	CMP R5, #3
	BEQ ALT
	BNE LOOP
	SWI 0X11
ALT:
	MOV R5, #0
	ADD R3, R3, #1
	CMP R9, #0
	BNE LOOP
	SWI 0X11